# Definir una lista de números

numeros = [10, 20, 30, 40]

# Definir una función que calcula la suma de una lista

def suma(lista):

  # Inicializar una variable para almacenar el resultado

  resultado = 0

  # Recorrer cada elemento de la lista

  for elemento in lista:

    # Sumar el elemento al resultado

    resultado = resultado + elemento

  # Devolver el resultado

  return resultado



# Llamar a la función con la lista de números y mostrar el resultado

print("La suma de los números es:", suma(numeros))

